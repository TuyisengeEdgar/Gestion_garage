    public void Table(){
    String [] client={"NOM-PIECE","PRIX","QUANTITE"};
    String [] afficher= new String[4];
    DefaultTableModel model= new DefaultTableModel(null,client);
    try{
    Connect();
    Statement st=con.createStatement();
    rs=st.executeQuery("select * from piece");
    
    while(rs.next()){
        //afficher[0]=rs.getString("idclient");
        afficher[0]=rs.getString("nomp");
        afficher[1]=rs.getString("prix");
        afficher[2]=rs.getString("quantite");
        model.addRow(afficher);
    }
    jTable1.setModel(model);
    con.close();
    }catch(Exception e){
    e.printStackTrace();
    }
    }
