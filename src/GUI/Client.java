     public void Table(){
    String [] client={"NOM","PRENOM","GENRE","Addresse","NUM-CARTE"};
    String [] afficher= new String[5];
    DefaultTableModel model= new DefaultTableModel(null,client);
    try{
    Connect();
    Statement st=con.createStatement();
    rs=st.executeQuery("select * from client");
    
    while(rs.next()){
        //afficher[0]=rs.getString("idclient");
        afficher[0]=rs.getString("nom");
        afficher[1]=rs.getString("prenom");
        afficher[2]=rs.getString("genre");
        afficher[3]=rs.getString("adresse");
        afficher[4]=rs.getString("num");
        model.addRow(afficher);
    }
    jTable1.setModel(model);
    con.close();
    }catch(Exception e){
    e.printStackTrace();
    }
    }
    
